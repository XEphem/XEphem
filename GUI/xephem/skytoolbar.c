/* callback from any of the Class TBs.
 * client is ct list.
 */
static void
svtb_ct_cb (w, client, call)
Widget w;
XtPointer client;
XtPointer call;
{
	int set = XmToggleButtonGetState(w);
	char *types = (char *)client;
	char tt[NOBJTYPES];
	char ct[NCLASSES];
	int ntypes;
	int i;

	svf_gettables (tt, ct);

	ntypes = strlen(types);
	for (i = 0; i < ntypes; i++) {
	    ct[(int)types[i]] = set;
	    if (types[i] == 'B')
		tt[BINARYSTAR] = set;
	}

	svf_settables (tt, ct);

	sv_all(mm_get_now());
}

/* callback from the solsys TB.
 * client is tt list.
 */
static void
svtb_tt_cb (w, client, call)
Widget w;
XtPointer client;
XtPointer call;
{
	int set = XmToggleButtonGetState(w);
	ObjType_t *types = (ObjType_t *)client;
	char tt[NOBJTYPES];
	char ct[NCLASSES];
	int i;

	svf_gettables (tt, ct);

	for (i = 0; i < NOBJTYPES; i++)
	    if (types[i])
		tt[(int)types[i]] = set;

	svf_settables (tt, ct);

	sv_all(mm_get_now());
}

